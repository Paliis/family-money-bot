# --- Google Sheets ---
scope = ["https://spreadsheets.google.com/feeds", "https://www.googleapis.com/auth/drive"]
google_creds_raw = base64.b64decode(os.environ["GOOGLE_CREDS_B64"]).decode("utf-8")
google_creds = json.loads(google_creds_raw)
creds = ServiceAccountCredentials.from_json_keyfile_dict(google_creds, scope)
client = gspread.authorize(creds)
sheet = client.open_by_key(os.environ["SPREADSHEET_ID"]).sheet1

pending_state = {}  # user_id: {step, amount, category}
report_state = {}   # user_id: waiting_for_report_range

# --- –ö–æ–º–∞–Ω–¥–∞ /start ---
def start_handler(update: Update, context: CallbackContext):
    update.message.reply_text("üëã –ü—Ä–∏–≤—ñ—Ç! –ù–∞–¥—ñ—à–ª–∏ —Å—É–º—É –≤–∏—Ç—Ä–∞—Ç, —ñ —è –¥–æ–ø–æ–º–æ–∂—É —ó—ó –∑–∞—Ñ—ñ–∫—Å—É–≤–∞—Ç–∏.")

# --- –ö–æ–º–∞–Ω–¥–∞ /report ---
def report_handler(update: Update, context: CallbackContext):
    user_id = update.message.from_user.id
    report_state[user_id] = "waiting_for_period"
    keyboard = [["–∑ –ø–æ—á–∞—Ç–∫—É –º—ñ—Å—è—Ü—è"], ["–≤—ñ–¥ –ó–ü"], ["–≤—ñ–¥ 2025-04-01"]]
    update.message.reply_text("–ó–∞ —è–∫–∏–π –ø–µ—Ä—ñ–æ–¥ –∑—Ä–æ–±–∏—Ç–∏ –∑–≤—ñ—Ç?", reply_markup=ReplyKeyboardMarkup(keyboard, one_time_keyboard=True, resize_keyboard=True))

# --- –§–æ—Ä–º—É–≤–∞–Ω–Ω—è –∑–≤—ñ—Ç—É ---
def send_report(update, start, end):
    rows = sheet.get_all_values()[1:]
    data = []
    for row in rows:
        try:
            dt = datetime.strptime(row[0], "%Y-%m-%d %H:%M")
            if not (start <= dt <= end): continue
            user, amount, cat, subcat = row[1], float(row[2]), row[3], row[4] if len(row) > 4 else ""
            data.append((user, amount, cat, subcat))
        except: continue

    if not data:
        update.message.reply_text("‚ùå –ó–∞ —Ü–µ–π –ø–µ—Ä—ñ–æ–¥ –Ω–µ–º–∞—î –¥–∞–Ω–∏—Ö.", reply_markup=ReplyKeyboardRemove())
        return

    income = sum(d[1] for d in data if d[1] > 0)
    expenses = [d for d in data if d[1] < 0]

    grouped = defaultdict(lambda: defaultdict(float))
    for _, amount, cat, subcat in expenses:
        grouped[cat][subcat] += amount

    lines = [f"üí∞ –ü—Ä–∏—Ö—ñ–¥: *{income:.2f} –≥—Ä–Ω*\n"]
    total_exp = 0
    for cat, subcats in sorted(grouped.items(), key=lambda x: sum(x[1].values())):
        cat_total = sum(subcats.values())
        total_exp += cat_total
        lines.append(f"\n*{cat}*: *{-cat_total:.2f} –≥—Ä–Ω*")
        for subcat, amt in subcats.items():
            if subcat:
                lines.append(f"  - {subcat}: {-amt:.2f} –≥—Ä–Ω")

    balance = income + total_exp
    lines.append(f"\nüìä –ü—ñ–¥—Å—É–º–æ–∫: *{balance:.2f} –≥—Ä–Ω*")

    update.message.reply_text("\n".join(lines), parse_mode="Markdown", reply_markup=ReplyKeyboardRemove())

# --- –ü–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –∑ —Å—É–º–æ—é ---
def handle_message(update: Update, context: CallbackContext):
    user_id = update.message.from_user.id
    user_name = update.message.from_user.first_name
    text = update.message.text.strip().lower()

    if report_state.get(user_id) == "waiting_for_period":
        del report_state[user_id]
        if text == "–∑ –ø–æ—á–∞—Ç–∫—É –º—ñ—Å—è—Ü—è":
            start = datetime.now().replace(day=1)
            end = datetime.now()
            return send_report(update, start, end)
        elif text == "–≤—ñ–¥ –∑–ø":
            update.message.reply_text("üîú –ö–æ–º–∞–Ω–¥–∞ –¥–æ–¥–∞–≤–∞–Ω–Ω—è –ø—Ä–∏—Ö–æ–¥—É –≤ —Ä–æ–∑—Ä–æ–±—Ü—ñ")
            return
        elif text.startswith("–≤—ñ–¥"):
            try:
                date_str = text.replace("–≤—ñ–¥", "").strip()
                start = datetime.strptime(date_str, "%Y-%m-%d")
                end = datetime.now()
                return send_report(update, start, end)
            except:
                update.message.reply_text("üìÖ –ù–µ–≤—ñ—Ä–Ω–∞ –¥–∞—Ç–∞. –§–æ—Ä–º–∞—Ç: –≤—ñ–¥ 2025-04-01")
                return

    state = pending_state.get(user_id, {})

    if state.get("step") == "await_category":
        category = text
        if category not in CATEGORY_MAP:
            keyboard = [[c] for c in CATEGORY_MAP.keys()]
            update.message.reply_text("–í–∏–±–µ—Ä–∏ –∫–∞—Ç–µ–≥–æ—Ä—ñ—é:", reply_markup=ReplyKeyboardMarkup(keyboard, one_time_keyboard=True, resize_keyboard=True))
            return
        if CATEGORY_MAP[category]:
            pending_state[user_id] = {"step": "await_subcategory", "amount": state["amount"], "category": category}
            subcat_keyboard = [[s] for s in CATEGORY_MAP[category]]
            update.message.reply_text("–û–±–µ—Ä–∏ –ø—ñ–¥–∫–∞—Ç–µ–≥–æ—Ä—ñ—é:", reply_markup=ReplyKeyboardMarkup(subcat_keyboard, one_time_keyboard=True, resize_keyboard=True))
            return
        amount = float(state["amount"])
        if category != "–ø—Ä–∏—Ö—ñ–¥":
            amount *= -1
        sheet.append_row([datetime.now().strftime("%Y-%m-%d %H:%M"), user_name, amount, category, ""])
        update.message.reply_text(f"üíæ –ó–∞–ø–∏—Å–∞–≤ {abs(amount)} –≥—Ä–Ω —É {category}", reply_markup=ReplyKeyboardRemove())
        pending_state.pop(user_id)
        return

    if state.get("step") == "await_subcategory":
        amount = float(state["amount"])
        if state["category"] != "–ø—Ä–∏—Ö—ñ–¥":
            amount *= -1
        sheet.append_row([datetime.now().strftime("%Y-%m-%d %H:%M"), user_name, amount, state["category"], text])
        update.message.reply_text(f"üíæ –ó–∞–ø–∏—Å–∞–≤ {abs(amount)} –≥—Ä–Ω —É {state['category']} > {text}", reply_markup=ReplyKeyboardRemove())
        pending_state.pop(user_id)
        return

    if text.replace(".", "", 1).isdigit():
        pending_state[user_id] = {"step": "await_category", "amount": text}
        keyboard = [[c] for c in CATEGORY_MAP.keys()]
        update.message.reply_text("–û–∫–µ–π, –æ–±–µ—Ä–∏ –∫–∞—Ç–µ–≥–æ—Ä—ñ—é:", reply_markup=ReplyKeyboardMarkup(keyboard, one_time_keyboard=True, resize_keyboard=True))
        return

    update.message.reply_text("üß† –ù–∞–¥—ñ—à–ª–∏ —Å—É–º—É —É —Ñ–æ—Ä–º–∞—Ç—ñ: 100 –∞–±–æ 250.5")

# --- –ó–∞–ø—É—Å–∫ ---
updater = Updater(os.environ["BOT_TOKEN"], use_context=True)
dp = updater.dispatcher
dp.add_handler(CommandHandler("start", start_handler))
dp.add_handler(CommandHandler("report", report_handler))
dp.add_handler(MessageHandler(Filters.text & ~Filters.command, handle_message))

updater.start_polling()
